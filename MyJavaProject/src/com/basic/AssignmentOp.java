package com.basic;

public class AssignmentOp {

	public static void main(String[] args) {
		int a=40;
		int b=20;
		a+=2;
		System.out.println(a);
		a-=10;
		System.out.println(a);
		a*=2;
		System.out.println(a);
		a/=8;
		System.out.println(a);
		a%=2;
		System.out.println(a);
		int min=a<b?a:b;
		System.out.println(min);
	}

}
